SELECT a.ACCEPT_NUMBER,a.PROJECT_NAME,c.COMPANY_NAME, a.CONTACT_PERSON,a.CONTACT_PERSON_MOBILE, high.DATA_VALUE AS HIGH_TECH_FIELD,subH.DATA_VALUE AS SUB_HIGH_TECH_FIELD, i.PROJECT_TYPE_NAME FROM env_project_base_info a
INNER JOIN env_company_base_info c ON c.ID = a.COMPANY_BASE_INFO_ID
LEFT JOIN env_system_business_item i ON a.PROJECT_TYPE = i.ITEM_ID
left JOIN project_project_group b on a.project_id = b.PROJECT_ID
LEFT JOIN p_data_dictionary high ON high.CODE = a.HIGH_TECH_FIELD
LEFT JOIN p_data_dictionary subH ON subH.CODE = a.SUB_HIGH_TECH_FIELD
WHERE a.ACCEPT_TIME > '2015-01-01' AND a.ACCEPT_TIME <'2015-05-20' AND a.ACCEPT_NUMBER <'20150520'
AND i.SYS_ID IN(87,101)
AND b.PROJECT_ID IS NULL
AND a.STATUS >=30
AND LENGTH(PROJECT_NAME)> 0
AND LENGTH(a.ACCEPT_NUMBER) > 0
ORDER BY a.ACCEPT_NUMBER ASC