

#未评审的项目列表

SELECT a.ACCEPT_NUMBER AS 受理号,a.PROJECT_NAME AS 项目名称, c.COMPANY_NAME AS 单位名称,IFNULL(ht.DATA_VALUE,a.HIGH_TECH_FIELD) AS 高新领域,
IFNULL(sht.DATA_VALUE, a.SUB_HIGH_TECH_FIELD) AS 子领域,IFNULL(b.PROJECT_TYPE_NAME,a.PROJECT_TYPE) AS 项目类型,
a.CONTACT_PERSON AS 联系人,a.CONTACT_PERSON_MOBILE AS 联系电话
FROM env_project_base_info a
LEFT JOIN env_system_business_item b ON a.PROJECT_TYPE = b.ITEM_ID
LEFT JOIN env_company_base_info c ON c.ID = a.COMPANY_BASE_INFO_ID
LEFT JOIN p_data_dictionary ht ON ht.CODE = a.HIGH_TECH_FIELD
LEFT JOIN p_data_dictionary sht ON sht.CODE = a.SUB_HIGH_TECH_FIELD
WHERE a.STATUS = 30 AND a.`YEAR` = 2015 AND a.ACCEPT_NUMBER IS NOT NULL
AND (b.SYS_ID IS NULL OR b.SYS_ID NOT IN(90,91))
ORDER BY a.ACCEPT_TIME ASC;